package kr.ac.exlang.model;

public class Board {
	String mName;
	String mNl;
	String mPl;
	String mHobby;
	int mAge;
	char mGender;
	String mIntro;
	String mImg;
	int mAuth;
	

	public String getmName() {
		return mName;
	}
	public void setmName(String mName) {
		this.mName = mName;
	}
	public String getmNl() {
		return mNl;
	}
	public void setmNl(String mNl) {
		this.mNl = mNl;
	}
	public String getmPl() {
		return mPl;
	}
	public void setmPl(String mPl) {
		this.mPl = mPl;
	}
	public String getmHobby() {
		return mHobby;
	}
	public void setmHobby(String mHobby) {
		this.mHobby = mHobby;
	}
	public int getmAge() {
		return mAge;
	}
	public void setmAge(int mAge) {
		this.mAge = mAge;
	}
	public char getmGender() {
		return mGender;
	}
	public void setmGender(char mGender) {
		this.mGender = mGender;
	}
	public String getmIntro() {
		return mIntro;
	}
	public void setmIntro(String mIntro) {
		this.mIntro = mIntro;
	}
	public String getmImg() {
		return mImg;
	}
	public void setmImg(String mImg) {
		this.mImg = mImg;
	}
	public int getmAuth() {
		return mAuth;
	}
	public void setmAuth(int mAuth) {
		this.mAuth = mAuth;
	}
	
	
	
}
